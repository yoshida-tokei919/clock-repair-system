/**
 * 住所文字列から都道府県を判別し、送料を返します。
 * 見つからない場合は 0 を返します。
 */
export function getShippingFeeByAddress(address: string): number {
    if (!address) return 0;

    // 都道府県リスト（最長一致のために文字数順、または単純に前方一致）
    const prefectures = Object.keys(SHIPPING_RATES);
    const matched = prefectures.find(pref => address.includes(pref));

    return matched ? SHIPPING_RATES[matched] : 0;
}
